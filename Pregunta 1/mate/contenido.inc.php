<div class="contenido">
        <p>La Carrera de Matemática es una Unidad Académica dependiente de la Facultad de Ciencias Puras y
            Naturales (FCPN) de la Universidad Mayor de San Andrés (UMSA), cuya misión principal es la
            formación profesional en Matemática a nivel superior y desarrollar la investigación matemática
            dentro del Instituto de Investigación Matemática (IIMAT), donde se ejecutan proyectos en las 
            diversas áreas de la matemática como Algebra, Análisis, Geometría, Topología, Optimización
            Matemática, Modelos Matemáticos, Análisis Numérico, Análisis Cuantitativo de Datos y en 
            otras áreas de aplicaciones matemáticas como proyectos multidisciplinarios o 
            transdisciplinarios. También, en el marco de una de las misiones de una universidad pública,
            la Carrera de Matemática desarrolla Proyectos de Interacción Social como las Olimpiadas 
            Matemáticas, Ferias de Matemática y Divulgación Matemática mediante la organización de cursos
            o eventos científicos  donde participan estudiantes, profesores de nivel escolar u otros
            profesionales interesados en la actualización de conocimientos en Matemática.
        </p>
        <h3>Mision</h3>
        <p>
            La Carrera de Matemática, como parte del Sistema Universitario público boliviano, es una institución de generación, transmisión y aplicación de conocimientos matemáticos, orientada hacia la investigación, la formación de profesionales calificados y al fortalecimiento de la enseñanza de la matemática a todo nivel.
        </p>
        <h3>Vision</h3>
        <p>
            Ser la Unidad nacional modelo de eficiencia, desarrollo e impacto social en Matemática que brinda formación sólida en pregrado y postgrado. Apoyar y contribuir al desarrollo científico y tecnológico de Bolivia a través de la resolución de problemas que competen a sus áreas de interés.
        </p>
    </div>